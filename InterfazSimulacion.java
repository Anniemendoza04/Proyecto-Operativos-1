    private void jButtonStartSimActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonStartSimActionPerformed
        // TODO add your handling code here:

        Timer timer = new Timer(1000, new ActionListener() { // Update every 1 second
            @Override
            public void actionPerformed(ActionEvent e) {
                updateTimeLabel(simulacion);
            }
        });
        timer.start();
    }//GEN-LAST:event_jButtonStartSimActionPerformed

    public void updateTimeLabel(SimuladorOS sim){
        String listos = sim.getColaListos().getFrente().toString();
        String bloqueados = sim.getColaBloqueados().getFrente().toString();
        String culminados = sim.getColasPrioridad().toString();
        
        int clock = sim.getCicloReloj();
        String clockText = String.valueOf(clock);
        SwingUtilities.invokeLater(() -> {
        
        
        jTextAreaListos.setText(listos);
        jTextAreaBloqueados.setText(bloqueados);
        jTextAreaCulminados.setText(culminados);
        jLabelClock.setText(clockText);
        });
    }
    private void jButtonCambiarProcesoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCambiarProcesoActionPerformed
        String algoritmoActualString = (String) jComboBoxPlanificacion.getSelectedItem();
        Planificador.AlgoritmoPlanificacion algoritmoActual = simulacion.getAlgoritmoPlanificacion();
        if (null != algoritmoActualString) switch (algoritmoActualString) {
            case "FCFS":
                algoritmoActual = Planificador.AlgoritmoPlanificacion.FCFS;
                break;
            case "SJF":
                algoritmoActual = Planificador.AlgoritmoPlanificacion.SJF;
                break;
            case "RoundRobin":
                algoritmoActual = Planificador.AlgoritmoPlanificacion.RoundRobin;
                break;
            case "SRT":
                algoritmoActual = Planificador.AlgoritmoPlanificacion.SRT;
                break;
            case "Feedback":
                algoritmoActual = Planificador.AlgoritmoPlanificacion.Feedback;
                break;
            default:
                break;
        }
   
        
    }//GEN-LAST:event_jButtonCambiarProcesoActionPerformed

