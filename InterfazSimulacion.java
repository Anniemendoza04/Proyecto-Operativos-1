    private void jButtonCrearProcesoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCrearProcesoActionPerformed
        // TODO add your handling code here:
        String nombre = jTextFieldProcessName.getText();
        int instrucciones = (Integer)jSpinnerCantInstrucciones.getValue();
        boolean tipo = jToggleButtonProcessType.isCursorSet();
        int exception = (Integer)jSpinnerGenExecpcion.getValue();
        int satisfy = (Integer)jSpinnerSatisfyExcep.getValue();
        int id = rand.nextInt();
        
        
        Proceso newProcess = new Proceso(id, nombre, Proceso.STATUS.READY, 0, instrucciones, tipo, exception, satisfy, 5);
        simulacion.getColaListos().encolar(newProcess);
    }//GEN-LAST:event_jButtonCrearProcesoActionPerformed

